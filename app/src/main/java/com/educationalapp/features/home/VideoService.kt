package com.educationalapp.features.home

import com.educationalapp.features.home.domain.model.Video

class VideoService {
    private var allVideos = mutableListOf<Video>()
    private var filteredVideos = mutableListOf<Video>()

    init {
        allVideos = (1..50).map {
            Video(
                id = it.toLong(),
                name = VIDEOS_NAME[it % VIDEOS_NAME.size],
                videoURL = VIDEOS_URL[it % VIDEOS_URL.size],
                likeStatus = null,
                likedCount = 0L,
                dislikedCount = 0L
            )
        }.toMutableList()
    }

    private fun updateFilteredList(newData: List<Video>) {
        filteredVideos.clear()
        filteredVideos.addAll(newData)
    }

    fun getVideos(): List<Video> {
        allVideos.sortBy { it.name }
        updateFilteredList(allVideos)
        return filteredVideos
    }

    fun getVideo(idVideo: Long): Video {
        return allVideos.first { it.id == idVideo }
    }

    fun reverseSort() {
        filteredVideos.reverse()
    }

    fun filterVideo(mask: String?) {
        allVideos.sortBy { it.name }
        updateFilteredList(
            if (mask.isNullOrBlank()) {
                allVideos
            } else {
                allVideos.filter {
                    it.name.contains(mask, true)
                }
            }
        )
    }

